# ─── Markdown Parser (for sections) ──────────────────────────────────────────

def _split_markdown_sections(markdown: str) -> dict:
    """Split the LLM markdown into a dict of {section_name: content}."""
    sections = {}
    current_key = None
    current_content = []

    for line in markdown.split("\n"):
        # Match ## Objective, ## Scope, ## Test Cases
        h2 = re.match(r"^## (.+)", line)
        # Match ### Inclusions, ### Test Environments etc.
        h3 = re.match(r"^### (.+)", line)
        h4 = re.match(r"^#### (.+)", line)
        h1 = re.match(r"^# (.+)", line)

        if h1:
            sections["title"] = h1.group(1).strip()
            current_key = None
            current_content = []
        elif h2:
            if current_key:
                sections[current_key] = "\n".join(current_content).strip()
            current_key = h2.group(1).strip().lower().replace(" ", "_")
            current_content = []
        elif h3 or h4:
            # Keep sub-headers inside parent section content
            if current_key:
                current_content.append(line)
        else:
            if current_key is not None:
                current_content.append(line)

    if current_key:
        sections[current_key] = "\n".join(current_content).strip()

    return sections


# ─── DOCX Export ─────────────────────────────────────────────────────────────

def to_docx(test_plan: dict, output_path: str | None = None) -> str:
    """Generate DOCX from test_plan record. Returns file path."""
    from docx import Document
    from docx.shared import Pt, RGBColor
    from docx.enum.text import WD_ALIGN_PARAGRAPH

    jira_id = test_plan["jira_id"]
    markdown = test_plan["content"]["markdown"]
    sections = _split_markdown_sections(markdown)

    # Allow yielding buffer if output_path strictly not used, 
    # but for local parity we still set a default filename string unless instructed otherwise
    use_buffer = False
    if output_path == "buffer":
        use_buffer = True
    elif not output_path:
        date_str = datetime.now().strftime("%Y%m%d")
        fname = f"{jira_id}_{date_str}.docx"
        output_path = str(EXPORTS_DIR / fname)

    doc = Document()

    # Title
    title_para = doc.add_heading(test_plan.get("title", f"Test Plan — {jira_id}"), level=1)

    meta = doc.add_paragraph(
        f"Generated by {test_plan.get('llm_model', 'LLM')} | "
        f"{_format_date(test_plan.get('generated_at', ''))} | "
        f"{test_plan.get('test_case_count', 0)} Test Cases"
    )
    meta.runs[0].font.color.rgb = RGBColor(0x80, 0x80, 0x80)
    meta.runs[0].font.size = Pt(9)

    # Ordered sections matching the template
    ordered_sections = [
        ("objective", "Objective", 2),
        ("scope", "Scope", 2),
    ]
    sub_sections = [
        ("inclusions", "Inclusions", 3),
        ("exclusion", "Exclusion", 3),
        ("test_environments", "Test Environments", 3),
        ("defect_reporting_procedure", "Defect Reporting Procedure", 3),
        ("test_strategy", "Test Strategy", 3),
        ("test_schedule", "Test Schedule", 3),
        ("test_deliverables", "Test Deliverables", 3),
        ("entry_and_exit_criteria", "Entry and Exit Criteria", 3),
        ("tools", "Tools", 4),
        ("risks_and_mitigations", "Risks and Mitigations", 4),
        ("approvals", "Approvals", 4),
    ]

    full_markdown_lines = markdown.split("\n")
    _write_sections_to_doc(doc, full_markdown_lines)

    # Test Cases section
    tc_list = test_plan["content"].get("test_cases", [])
    if tc_list:
        doc.add_heading("Test Cases", level=2)
        for tc in tc_list:
            _write_test_case_to_doc(doc, tc)

    if use_buffer:
        import io
        buffer = io.BytesIO()
        doc.save(buffer)
        buffer.seek(0)
        return buffer
        
    doc.save(output_path)
    return output_path


def _write_sections_to_doc(doc, lines):
    """Parse markdown lines and write to docx with proper heading levels."""
    from docx.shared import Pt
    skip_tc_section = False

    for line in lines:
        if re.match(r"^## Test Cases", line):
            skip_tc_section = True
            continue
        if skip_tc_section and re.match(r"^### TC-", line):
            continue
        if skip_tc_section:
            continue

        if re.match(r"^# ", line):
            continue  # Already written as title
        elif re.match(r"^## ", line):
            doc.add_heading(line[3:].strip(), level=2)
        elif re.match(r"^### ", line):
            doc.add_heading(line[4:].strip(), level=3)
        elif re.match(r"^#### ", line):
            doc.add_heading(line[5:].strip(), level=4)
        elif re.match(r"^\|", line):
            # Table row — group and write
            _add_table_line(doc, line)
        elif re.match(r"^\*\*(.+)\*\*:", line):
            # Bold key line
            p = doc.add_paragraph()
            parts = re.split(r"(\*\*.+?\*\*)", line)
            for part in parts:
                if part.startswith("**") and part.endswith("**"):
                    run = p.add_run(part[2:-2])
                    run.bold = True
                else:
                    p.add_run(part)
        elif re.match(r"^[-*•] ", line) or re.match(r"^\s+[-*•] ", line):
            p = doc.add_paragraph(line.lstrip("-*• \t"), style="List Bullet")
        elif re.match(r"^\d+\. ", line):
            p = doc.add_paragraph(re.sub(r"^\d+\. ", "", line), style="List Number")
        elif line.strip():
            doc.add_paragraph(_strip_md_bold(line))


def _strip_md_bold(text: str) -> str:
    return re.sub(r"\*\*(.+?)\*\*", r"\1", text)


